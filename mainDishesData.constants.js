const mainCardGroup = document.getElementById("mainCardGroup");

MAINDISHES_DATA.forEach((card) => {
  const cardContainer = document.createElement("div");
  cardContainer.classList.add("Card__Container");

  const cardBody = document.createElement("div");
  cardBody.classList.add("Card__Body");

  const imageWrapper = document.createElement("div");
  imageWrapper.classList.add("Card__Image__Wrapper");

  const image = document.createElement("div");
  image.classList.add("Card__Image");
  const img = document.createElement("img");
  img.src = card.image;
  img.alt = card.title;
  image.appendChild(img);

  imageWrapper.appendChild(image);

  const content = document.createElement("div");
  content.classList.add("Card__Content");

  const title = document.createElement("div");
  title.classList.add("Card__Title");
  title.textContent = card.title;

  const description = document.createElement("div");
  description.classList.add("Card__Description");
  description.textContent = card.description;

  const flag = document.createElement("div");
  flag.classList.add("Card__Flag");

  const price = document.createElement("div");
  price.classList.add("Card__Price");
  price.textContent = card.price;

  content.appendChild(title);
  content.appendChild(description);
  content.appendChild(flag);
  content.appendChild(price);

  cardBody.appendChild(imageWrapper);
  cardBody.appendChild(content);
  
  cardContainer.appendChild(cardBody);

  mainCardGroup.appendChild(cardContainer);
});
